// Get card price from database
function getCardPrice(cardName, setCode) {
  const setPrices = CARD_PRICES[setCode];
  if (!setPrices) return null;

  // Check for specific card price
  if (setPrices[cardName] !== undefined && !cardName.startsWith('_')) {
    return setPrices[cardName];
  }

  // Return null to indicate we should use default pricing
  return null;
}

// Get default price by rarity (when no specific price exists)
function getDefaultPrice(setCode, rarity) {
  const setPrices = CARD_PRICES[setCode];
  if (!setPrices) {
    // Fallback defaults
    const fallback = { common: 25, uncommon: 60, rare: 200, land: 40 };
    return fallback[rarity] || 25;
  }

  const key = '_' + rarity;
  return setPrices[key] || 25;
}

// Format price for display (abbreviated)
function formatPrice(price) {
  if (price === null || price === undefined) return '...';
  if (price >= 1000) {
    return '$' + (price / 1000).toFixed(1) + 'k';
  }
  return '$' + price.toLocaleString(undefined, { minimumFractionDigits: 0, maximumFractionDigits: 0 });
}

// Format price with full precision
function formatPriceFull(price) {
  if (price === null || price === undefined) return '$0';
  return '$' + price.toLocaleString(undefined, { minimumFractionDigits: 0, maximumFractionDigits: 0 });
}

